package staff.model;

public class Prayer {
	private int id;
	private String prayerName;
	
	public Prayer() {
		
	}


	public int getId() {
		return id;
	}



	public void setId(int id) {
		this.id = id;
	}



	public String getPrayerName() {
		return prayerName;
	}

	public void setPrayerName(String prayerName) {
		this.prayerName = prayerName;
	}
	
	
}
